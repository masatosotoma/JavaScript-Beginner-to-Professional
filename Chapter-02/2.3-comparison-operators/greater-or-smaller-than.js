let x = 5;
let y = 6;

console.log(y > x); // to compare two values if the left side value is greater than the right side value. Will return true or false.

console.log(x > y); // to return false

console.log(y > y); // since the values are same, it will return false

console.log(y >= y); // it will compare value whether y is greater than or equal to y.

console.log(y < x); // to check two values if the left side value is smaller than the right side value.

console.log(x < y); // to return true

console.log(y < y); // to return false because the values are the same

console.log(y <= y); // to return true because the values are equal.
